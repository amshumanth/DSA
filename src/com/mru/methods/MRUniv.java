package com.mru.methods;

public class MRUniv {
	//no parameter and no return type
	public static void dsa1() {
		System.out.println("Welcome to DSA1");
	}
	//with parameter and no return type
	public  static void dsa2(String name) {
		System.out.println(name);
	}
	//no parameter and return type
	public  static String dsa3()
	{
		return "Welcome to LeetCode";
	}
	//with parameter and with return type
	public  static String dsa4(String name) {
		return "Welcome to "+name;
	}

}
